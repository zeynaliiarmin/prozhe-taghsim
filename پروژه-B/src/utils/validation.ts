export const growthStatus = (val: number, min: number, med: number, max: number) => {
  const sd = (max - min) / 4;
  if (val >= min + sd && val <= max - sd) return { label: 'نرمال', color: '#22c55e' };
  if (val >= min && val <= max) return { label: 'نزدیک به مرز', color: '#f97316' };
  const diff = val < min ? min - val : val - max;
  if (diff <= sd * 1.5) return { label: val < min ? 'زیر نرمال' : 'بالای نرمال', color: '#f97316' };
  return { label: val < min ? 'خیلی زیر نرمال' : 'خیلی بالای نرمال', color: '#ef4444' };
};